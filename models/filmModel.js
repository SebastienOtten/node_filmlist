class Film {
    nomdufilm ;
    realisateur ;
    acteur ;
    synopsis ;
    note ;
    commentaire ;

    constructor(nomdufilm, realisateur, acteur, synopsis, note, commentaire) {
        this.nomdufilm = nomdufilm;
        this.realisateur = realisateur;
        this.acteur = acteur;
        this.synopsis = synopsis;
        this.note = note;
        this.commentaire = commentaire;
    }
};